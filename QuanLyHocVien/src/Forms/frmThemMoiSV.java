    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        boolean checkResult = txtHoVaTen.getText().equals("") || txtNgaySinh.getText().equals("") || txtDiem.getText().equals("") || txtDiaChi.getText().equals("") || txtMaSinhVien.getText().equals("");
        if (checkResult) {
            if (txtHoVaTen.getText().equals("")) {
                lbLoiHoVaTen.setText("Nhập họ và tên");
            }
            if (txtNgaySinh.getText().equals("")) {
                lbLoiNgaySinh.setText("Nhập ngày sinh");
            }
            if (txtDiem.getText().equals("")) {
                lbLoiDiem.setText("Nhập điểm");
            }
            if (txtDiaChi.getText().equals("")) {
                lbLoiDiaChi.setText("Nhập địa chỉ");
            }
            if(txtMaSinhVien.getText().equals("")) {
                lbLoiMaSinhVien.setText("Nhập địa chỉ");
            }
        }
        else {
            tbSinhVien sv = new tbSinhVien();
            masv = txtMaSinhVien.getText();
            ten = txtHoVaTen.getText();
            ngaysinh = txtNgaySinh.getText();
            diem = Float.parseFloat(txtDiem.getText());
            diachi = txtDiaChi.getText();
            malophoc = cbLopHoc.getSelectedIndex() +1;
            if (rbNam.isSelected()) {
                gioitinh = 1;
            }
            else {
                gioitinh = 2;
            }
            if (anh == null) {
                anh = "";
            }
            boolean result = sv.themMoiSV(masv, ten, ngaysinh, diem, diachi, gioitinh, malophoc, anh);
            if (result) {
                JOptionPane.showMessageDialog(this, "Thêm mới thành công");
                fql.getBtnThemSinhVien().setEnabled(true);
                jPQL.loadContent("");
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(this, "Thêm mới thất bại");
            }
        }
        
    }//GEN-LAST:event_btnThemMoiActionPerformed

