    private void btnThemLopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLopActionPerformed
        // TODO add your handling code here:
        tbLopHoc lophoc = new tbLopHoc();
        Vector<clsLopHoc> bangLopHoc = lophoc.bangLopHoc();
        
        if (txtTenLop.getText().equals("")) {
            lbLoiTenLop.setText("Nhập tên lớp !");
            return;
        }
        if (!lophoc.timKiem(txtTenLop.getText())) {
            lbLoiTenLop.setText("Tên lớp đã tồn tại");
            return;
        }
        else if (lophoc.themMoiLopHoc(txtTenLop.getText())) {
            JOptionPane.showMessageDialog(this, "Thêm mới thành công !");
            fql.getBtnThemSinhVien().setEnabled(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_btnThemLopActionPerformed

